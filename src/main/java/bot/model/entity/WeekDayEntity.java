package bot.model.entity;

public class WeekDayEntity {

    private Integer id;
    private String day;

    public WeekDayEntity() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
}
